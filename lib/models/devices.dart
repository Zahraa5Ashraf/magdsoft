class DeviceItem {
  final int id;
  final String company;
  final String name;
  final String type;
  final String price;
  final String image;
  final String description;

  DeviceItem({
    required this.id,
    required this.company,
    required this.name,
    required this.type,
    required this.price,
    required this.image,
    required this.description,
  });
}
